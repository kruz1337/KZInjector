	inline char* GetArgValue(char** argv, const char* key, const char* keyAlt) {
		for (argv++; *argv; argv++) {
			if (strcmp(*argv, key) == 0 || strcmp(*argv, keyAlt) == 0) {
				return *++argv;
			}
		}

		return nullptr;
	}

	inline bool SearchFile(const char* fileName, const char* directory, bool subdirs, char** oPath) {
		DIR* dir = opendir(directory);
		if (dir == nullptr) {
			closedir(dir);
			return false;
		}

		dirent* dirEnt;
		while ((dirEnt = readdir(dir)) != nullptr) {
			if (strcmp(dirEnt->d_name, ".") == 0 || strcmp(dirEnt->d_name, "..") == 0) {
				continue;
			}

			char* spath = new char[MAX_PATH];
			sprintf(spath, "%s\\%s", directory, dirEnt->d_name);

			struct stat dirStat;
			stat(spath, &dirStat);

			if (S_ISDIR(dirStat.st_mode)) {
				if (subdirs && SearchFile(fileName, spath, subdirs, oPath)) {
					free(spath);
					closedir(dir);
 					return true;
				}
				continue;
			}

			if (strcmpi(fileName, dirEnt->d_name) == 0) {
				strcpy(*oPath, spath);
				free(spath);
				closedir(dir);
 				return true;
			}

 		}

		closedir(dir);

		return false;
	}
